import UIKit


extension UIColor {
    enum app {
        static var black: UIColor {
            return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
        }
        
        static var white: UIColor {
            return #colorLiteral(red: 1.0, green: 1.0, blue: 1.0, alpha: 1.0)
        }
    }
}

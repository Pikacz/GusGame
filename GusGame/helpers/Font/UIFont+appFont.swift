import UIKit


extension UIFont {
    enum app {
        static var standard_17: UIFont {
            return UIFont.systemFont(ofSize: CGFloat(17.0))
        }
        
        static var standard_21: UIFont {
            return UIFont.systemFont(ofSize: CGFloat(21.0))
        }
        
        static var standard_33: UIFont {
            return UIFont.systemFont(ofSize: CGFloat(33.0))
        }
    }
}
